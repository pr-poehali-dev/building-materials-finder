
CREATE TABLE IF NOT EXISTS t_p34696980_building_materials_f.districts (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    slug VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS t_p34696980_building_materials_f.categories (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    slug VARCHAR(50) NOT NULL UNIQUE,
    icon VARCHAR(50) DEFAULT 'Package'
);

CREATE TABLE IF NOT EXISTS t_p34696980_building_materials_f.shops (
    id SERIAL PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    district_id INTEGER REFERENCES t_p34696980_building_materials_f.districts(id),
    address VARCHAR(300),
    phone VARCHAR(50),
    working_hours VARCHAR(100),
    rating NUMERIC(2,1) DEFAULT 0,
    map_x NUMERIC(5,2),
    map_y NUMERIC(5,2)
);

CREATE TABLE IF NOT EXISTS t_p34696980_building_materials_f.products (
    id SERIAL PRIMARY KEY,
    name VARCHAR(300) NOT NULL,
    category_id INTEGER REFERENCES t_p34696980_building_materials_f.categories(id),
    description TEXT,
    unit VARCHAR(30) DEFAULT 'шт',
    brand VARCHAR(100),
    image_url TEXT
);

CREATE TABLE IF NOT EXISTS t_p34696980_building_materials_f.offers (
    id SERIAL PRIMARY KEY,
    product_id INTEGER REFERENCES t_p34696980_building_materials_f.products(id),
    shop_id INTEGER REFERENCES t_p34696980_building_materials_f.shops(id),
    price NUMERIC(10,2) NOT NULL,
    in_stock BOOLEAN DEFAULT TRUE,
    updated_at TIMESTAMP DEFAULT NOW()
);

